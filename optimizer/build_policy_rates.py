    for ccy, hist in RATES.items():
        for d, r in hist:
            rows.append({'date': d, 'currency': ccy, 'rate': r})
    df = pd.DataFrame(rows)
    df['date'] = pd.to_datetime(df['date'])
    df = df.sort_values(['currency', 'date']).reset_index(drop=True)
